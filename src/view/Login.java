    private void btn_log_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_log_loginActionPerformed
        // TODO add your handling code here:
        
        User usr = new User();
        
        String username = tf_log_uname.getText().trim();
        String password = new String(pf_log_pword.getPassword());

        int userStatus = usr.RetrieveUserStatus(username, password);

        if(username.equals("") || password.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Fields cannot be empty");
        }
        else {
            if (userStatus != -1) {
            JOptionPane.showMessageDialog(null, "Login Successfully");

            if (userStatus == 2) {
                Order ord = new Order();
                ord.setVisible(true);
                dispose();
            } else if (userStatus == 3) {
                StaffDashboard sd = new StaffDashboard();
                sd.setVisible(true);
                dispose();
            }
            } else {
                JOptionPane.showMessageDialog(null, "Wrong Credentials");
            }
        }
    }//GEN-LAST:event_btn_log_loginActionPerformed

