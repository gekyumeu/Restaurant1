    private void pf_log_pwordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pf_log_pwordKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
            User usr = new User();
        
            String username = tf_log_uname.getText().trim();
            String password = new String(pf_log_pword.getPassword());

            int userStatus = usr.RetrieveUserStatus(username, password);

            if (userStatus != -1) {
                JOptionPane.showMessageDialog(null, "Login Successfully");

                if (userStatus == 2) {
                    Order ord = new Order();
                    ord.setVisible(true);
                    dispose();
                } else if (userStatus == 3) {
                    StaffDashboard sd = new StaffDashboard();
                    sd.setVisible(true);
                    dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Wrong Credentials");
            }
        }
    }//GEN-LAST:event_pf_log_pwordKeyPressed

